    val fpSideeffects: String?,
    val otherSideEffects: String?,
    val fpMethodFollowup: String?,
    val otherFollowUpForFpMethod: String?,
    val subVisitCategory: String?,
    val followUpForFpMethod: String?,
    val sideEffects: String?,
)

@JsonClass(generateAdapter = true)
data class BenPhysicalVitalDetail(
    val ID: Long?,
    val beneficiaryRegID: Long?,
    val benVisitID: Long?,
    val providerServiceMapID: Int?,
    val visitCode: Long?,
    val temperature: Double?,
    val pulseRate: Int?,
    val respiratoryRate: Int?,
    val systolicBP_1stReading: Int?,
    val diastolicBP_1stReading: Int?,
    val systolicBP_2ndReading: Int?,
    val diastolicBP_2ndReading: Int?,
    val systolicBP_3rdReading: Int?,
    val diastolicBP_3rdReading: Int?,
    val bloodPressureStatusID: Int?,
    val bloodPressureStatus: String?,
    val bloodGlucose_Fasting: Int?,
    val bloodGlucose_Random: Int?,
    val bloodGlucose_2hr_PP: Int?,
    val bloodGlucose_NotSpecified: Int?,
    val diabeticStatusID: Int?,
    val diabeticStatus: String?,
    val averageSystolicBP: Int?,
    val averageDiastolicBP: Int?,
    val capillaryRefillTime: String?,
    val rbsTestResult: String?,
    val rbsTestRemarks: String?,
    val sPO2: String?,
    val hemoglobin: Double?,
    val deleted: Boolean?,
    val processed: String?,
)

@JsonClass(generateAdapter = true)
data class BenChiefComplaints(
    val benChiefComplaintID: Long?,
    val beneficiaryRegID: Long?,
    val benVisitID: Long?,
    val providerServiceMapID: Int?,
    val visitCode: Long?,
    val chiefComplaintID: Int?,
    val chiefComplaint: String?,
    val conceptID: String?,
    val duration: Int?,
    val unitOfDuration: String?,
    val description: String?,
    val deleted: Boolean?,
    val processed: String?
)

