    val pregnancyStatus: String?,
    val rCHID: String?,
    val healthFacilityType: String?,
    val healthFacilityLocation: String?,
    val reportFilePath: String?,
    val deleted: Boolean?,
    val processed: String?,
    val visitFlowStatusFlag: String?,
